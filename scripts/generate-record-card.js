function updateManifest(recordIdValue, recordCardPath, pdf) {
  const manifestPath = path.join(process.cwd(), "data/app/property-manifest.json");
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  const entry = {
    id: recordIdValue,
    label: `${pdf.owner} property`,
    propertyClass: pdf.classification.propertyClass || pdf.accountType,
    parcelId: pdf.ntoParcelId,
    situsAddress: pdf.situsAddress,
    county: "gage",
    taxDistrict: pdf.taxDistrict,
    recordCardPath,
    recordCardStatus: "available"
  };
  const index = manifest.properties.findIndex(property => property.id === recordIdValue);
  if (index === -1) {
    manifest.properties.push(entry);
  } else {
    manifest.properties[index] = { ...manifest.properties[index], ...entry };
  }
  fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
}

