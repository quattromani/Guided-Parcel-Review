function usage() {
  console.error([
    "Usage: node scripts/generate-record-card.js /path/to/parcel.pdf [--nto-capture path] [--update-manifest]",
    "       node scripts/generate-record-card.js /path/to/parcel.pdf [--valuation-group 3] [--market-area \"Beatrice & Beatrice Subs\"]",
    "",
    "The script expects an NTO capture generated by:",
    "  node scripts/capture-nto-statements.js {nto-parcel-id}"
  ].join("\n"));
  process.exit(1);
}

function parseArgs(argv) {
  const args = {
    pdfPath: null,
    ntoCapturePath: null,
    valuationGroup: null,
    marketArea: null,
    marketGroup: null,
    updateManifest: false
  };

  for (let index = 2; index < argv.length; index += 1) {
    const value = argv[index];
    if (value === "--nto-capture") {
      args.ntoCapturePath = argv[++index];
    } else if (value === "--valuation-group") {
      args.valuationGroup = argv[++index];
    } else if (value === "--market-area") {
      args.marketArea = argv[++index];
    } else if (value === "--market-group") {
      args.marketGroup = argv[++index];
    } else if (value === "--update-manifest") {
      args.updateManifest = true;
    } else if (!args.pdfPath) {
      args.pdfPath = value;
    } else {
      usage();
    }
  }

  if (!args.pdfPath) usage();
  return args;
}

function money(value) {
  if (!value || value === "—") return 0;
  const sign = /^\(/.test(value.trim()) ? -1 : 1;
  const parsed = Number(value.replace(/[($,\)]/g, ""));
  return Number.isFinite(parsed) ? sign * parsed : 0;
}

function formatMoney(value) {
  return `$${Number(value || 0).toLocaleString("en-US", { minimumFractionDigits: 0, maximumFractionDigits: 2 })}`;
}

function normalizeAddress(value) {
  return value ? value.replace(/\s+/g, " ").replace(" BEATRICE", ", BEATRICE").trim() : null;
}

function jpegDimensions(filePath) {
  const buffer = fs.readFileSync(filePath);
  let offset = 2;

  while (offset < buffer.length) {
    if (buffer[offset] !== 0xff) break;
    const marker = buffer[offset + 1];
    const length = buffer.readUInt16BE(offset + 2);

    if (marker >= 0xc0 && marker <= 0xc3) {
      return {
        height: buffer.readUInt16BE(offset + 5),
        width: buffer.readUInt16BE(offset + 7)
      };
    }

    offset += 2 + length;
  }

  return { width: 0, height: 0 };
}

function extractPdfAssets(pdfPath, parcelId) {
  const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), "record-card-assets-"));
  const outputPrefix = path.join(tempDir, "asset");

  try {
    execFileSync("pdfimages", ["-j", pdfPath, outputPrefix], { stdio: "ignore" });
  } catch {
    return { photo: null, sketch: null, additionalPhotos: [] };
  }

  const candidates = fs.readdirSync(tempDir)
    .filter(file => file.toLowerCase().endsWith(".jpg"))
    .map(file => {
      const sourcePath = path.join(tempDir, file);
      const dimensions = jpegDimensions(sourcePath);
      return {
        sourcePath,
        ...dimensions,
        fileSize: fs.statSync(sourcePath).size,
        area: dimensions.width * dimensions.height
      };
    })
    .filter(image => image.width >= 400 && image.height >= 250)
    .sort((a, b) => b.area - a.area);

  if (!candidates.length) {
    return { photo: null, sketch: null, additionalPhotos: [] };
  }

  fs.mkdirSync(path.resolve("assets/images"), { recursive: true });

  const [sketchCandidate] = candidates
    .slice()
    .sort((a, b) => a.fileSize - b.fileSize || a.area - b.area);
  const [photoCandidate] = candidates
    .filter(candidate => candidate !== sketchCandidate);

  const copyAsset = (candidate, suffix) => {
    if (!candidate) return null;
    const relativePath = `assets/images/${parcelId}_${suffix}.jpg`;
    fs.copyFileSync(candidate.sourcePath, path.resolve(relativePath));
    return relativePath;
  };
  const additionalCaption = (candidate, index) => {
    const label = candidate.fileSize < 12000 ? "Additional Property Sketch" : "Additional Property Photo";
    return `${label} ${index + 1}`;
  };

  return {
    photo: copyAsset(photoCandidate, 1),
    sketch: copyAsset(sketchCandidate, 2),
    additionalPhotos: candidates
      .filter(candidate => candidate !== photoCandidate && candidate !== sketchCandidate)
      .map((candidate, index) => ({
        src: copyAsset(candidate, index + 3),
        caption: additionalCaption(candidate, index)
      }))
      .filter(item => item.src)
  };
}

function extractPdfImageLinks(pdfPath) {
  try {
    const output = execFileSync("pdfinfo", ["-url", pdfPath], {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"]
    });
    const links = [...output.matchAll(/https?:\/\/\S+/g)].map(match => match[0]);
    return [...new Set(links)].sort((left, right) => {
      const leftIndex = Number(left.match(/_(\d+)\.[a-z]+$/i)?.[1] || Number.MAX_SAFE_INTEGER);
      const rightIndex = Number(right.match(/_(\d+)\.[a-z]+$/i)?.[1] || Number.MAX_SAFE_INTEGER);
      return leftIndex - rightIndex || left.localeCompare(right);
    });
  } catch {
    return [];
  }
}

function firstMatch(text, pattern, fallback = null) {
  const match = text.match(pattern);
  return match ? match[1].trim().replace(/\s+/g, " ") : fallback;
}

function roundCurrency(value) {
  return Math.round((value || 0) * 100) / 100;
}

function parseStatementHistoryRow(text, year) {
  const pattern = new RegExp(`${year}\\s+VIEW DETAILS\\s+\\S+\\s+•\\s*([0-9 /]+)\\s+([A-Z+]+)\\s+(\\$[\\d,.]+)\\s+(\\$[\\d,.]+)\\s+(\\(?\\$[\\d,.]+\\)?)\\s+(\\$[\\d,.]+)\\s+(\\$[\\d,.]+)\\s+(\\$[\\d,.]+)`);
  const match = text.match(pattern);
  if (!match) return null;

  return {
    statementNumber: match[1].trim(),
    statementType: match[2],
    grossTaxAmount: money(match[3]),
    penalty: money(match[4]),
    totalCredit: money(match[5]),
    netAmountDue: money(match[6]),
    totalPaid: money(match[7]),
    taxDue: money(match[8])
  };
}

function fallbackDistributionRows(pdf, assessedValue, grossTaxAmount) {
  if (!pdf?.levyRows?.length || !assessedValue) return [];

  return pdf.levyRows.map(row => {
    const amount = roundCurrency((assessedValue * row.rate) / 100);
    return {
      authority: row.description,
      levy: row.rate,
      amount,
      share: grossTaxAmount ? Math.round((amount / grossTaxAmount) * 1000) / 10 : 0
    };
  });
}

function parseNtoRecord(record, pdf = null) {
  const text = record.text;
  const statementMatch = text.match(/TAX STATEMENTS\s+([A-Z+]+)\s+No\.\s+([0-9 /]+)\s+DISTRICT\s+([0-9]+)/);
  const assessedMatch = text.match(/#([0-9]+)\s+([A-Z]+)\s+\$([\d,]+)\s+\$([\d,]+)\s+\$([\d,]+)\s+(?:\$([\d,]+)|—)/);
  const historyRow = parseStatementHistoryRow(text, record.year);
  const pdfValue = pdf?.assessedValues?.find(row => row.year === record.year);
  const totalCredit = money(firstMatch(text, /TOTAL\s+All Statements\s+Gross\s+\$[\d,.]+\s+Credits\s+(\(?\$[\d,.]+\)?)/)) || historyRow?.totalCredit || 0;
  const grossTax = money(firstMatch(text, /Gross Tax\s+(\$[\d,.]+)/)) || historyRow?.grossTaxAmount || 0;
  const netTax = money(firstMatch(text, /NET TAX\s+(\$[\d,.]+)/)) || historyRow?.netAmountDue || 0;
  const totalPaid = money(firstMatch(text, /TOTAL POSTED:\s+(\$[\d,.]+)/)) || historyRow?.totalPaid || 0;
  const assessedValue = money(firstMatch(text, /ASSESSED VALUATIONS\s+TOTAL:\s+(\$[\d,]+)/)) || pdfValue?.total || 0;
  const building = assessedMatch ? money(`$${assessedMatch[4]}`) : (pdfValue?.dwelling ?? null);
  const land = assessedMatch ? money(`$${assessedMatch[5]}`) : (pdfValue?.land ?? null);
  const other = assessedMatch ? money(assessedMatch[6] ? `$${assessedMatch[6]}` : "—") : (pdfValue?.outbuilding ?? 0);
  const taxDue = Math.round((netTax - totalPaid) * 100) / 100;

  let distributionRows = [...text.matchAll(/^\s*([0-9]+:\s+.+?)\s+([0-9]\.[0-9]{6})\s+\$([\d,]+\.\d{2})\s*\n([0-9.]+)%/gm)]
    .map(match => ({
      authority: match[1].trim().replace(/\s+/g, " "),
      levy: Number(match[2]),
      amount: money(`$${match[3]}`),
      share: Number(match[4])
    }));

  if (!distributionRows.length) {
    distributionRows = fallbackDistributionRows(pdf, assessedValue, grossTax);
  }

  const totalLevy = Number(firstMatch(text, /\n TOTAL\s+([0-9]\.[0-9]{6})\s+\$[\d,.]+\s+100\.0%/))
    || (distributionRows.length ? Math.round(distributionRows.reduce((sum, row) => sum + row.levy, 0) * 1000000) / 1000000 : null);

  return {
    year: record.year,
    statementNumber: statementMatch?.[2]?.trim() || assessedMatch?.[1] || historyRow?.statementNumber || null,
    statementType: statementMatch?.[1] || assessedMatch?.[2] || historyRow?.statementType || "REAL",
    district: statementMatch?.[3] || pdf?.taxDistrict || null,
    assessedValue,
    building,
    land,
    other,
    grossTaxAmount: grossTax,
    schoolCredit: money(firstMatch(text, /School Credit\s+(\(?\$[\d,.]+\)?)/)),
    nonAgCredit: money(firstMatch(text, /Non-Ag Credit\s+(\(?\$[\d,.]+\)?)/)),
    agLandCredit: money(firstMatch(text, /Ag Land Credit\s+(\(?\$[\d,.]+\)?)/)),
    totalCredit,
    netAmountDue: netTax,
    totalPaid,
    taxDue,
    totalLevy,
    distributionRows
  };
}

function levyGroup(authority) {
  const value = authority.toLowerCase();
  if (value.includes("sch")) return "School";
  if (value.includes("city")) return "City";
  if (value.includes("county") || value.includes("cnty")) return "County";
  if (value.includes("nrd")) return "Natural resources";
  if (value.includes("scc")) return "Community college";
  if (value.includes("esu")) return "Education service";
  return "Other";
}

function taxStatement(row) {
  return {
    taxYear: row.year,
    statementNumber: row.statementNumber,
    statementType: row.statementType,
    totalTaxesDue: row.netAmountDue,
    dueDate: null,
    assessedValue: row.assessedValue,
    grossTaxAmount: row.grossTaxAmount,
    credits: {
      homestead: { value: 0, amount: 0 },
      nonAgTax: { value: null, amount: row.nonAgCredit },
      agLandTax: { value: null, amount: row.agLandCredit },
      schoolTax: { value: null, amount: row.schoolCredit }
    },
    netAmountDue: row.netAmountDue,
    totalPaid: row.totalPaid,
    taxDue: row.taxDue,
    derived: {
      grossLevyRate: row.totalLevy ? row.totalLevy / 100 : (row.assessedValue ? row.grossTaxAmount / row.assessedValue : null),
      netEffectiveTaxRate: row.assessedValue ? row.netAmountDue / row.assessedValue : null,
      totalCreditAmount: row.totalCredit
    },
    sourceNote: "Nebraska Taxes Online statement detail modal.",
    land: row.land,
    dwelling: row.building,
    outbuilding: row.other
  };
}

